private boolean validasiinput(){
    boolean valid = false;
    if(txtpassword.getText().trim().isEmpty() && txtusername.getText().trim().isEmpty()){
        JOptionPane.showMessageDialog(null, "Input username & password"+ attempt);
    }else if(txtusername.getText().trim().isEmpty()){
        JOptionPane.showMessageDialog(null, "Username tidak boleh kosong" + attempt);
    }else if(txtpassword.getText().trim().isEmpty()){
        JOptionPane.showMessageDialog(null, "Pasword tidak boleh kosong" + attempt);
    }else{
        JOptionPane.showMessageDialog(null, "Password atau username yang anda input salah" +attempt);
    }   
    return valid;
        
}

