    private void myButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_myButton1ActionPerformed
        // TODO add your handling code here:
    String sql = "SELECT * FROM user WHERE username = '"+txtusername.getText()+"'AND password='"+txtpassword.getText()+"'";
    //validasiinput();
    if(attempt <= 6){
        try{
            java.sql.Connection conn = (Connection)koneksi.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            java.sql.ResultSet rs = pst.executeQuery(sql);
            
            if(rs.next()){
                
                txtusername.setText(rs.getString(4));
                txtpassword.setText(rs.getString(5));
                
                txt_namaKasirA.setText(rs.getString(2));
                txtnikA.setText(rs.getString(1));
                
                txt_namaKasir.setText(rs.getString(2));
                txtnik.setText(rs.getString(1));
                
                txtnamapetugas.setText(rs.getString(2));
                txtkodepetugas.setText(rs.getString(1));
                
                if(rs.getString("jabatan").equals("admin")){
                    Session.set_username(rs.getString("nama_lengkap"));
                    
                    dispose();
                    new Dashboard().setVisible(true);
                }else if (rs.getString("jabatan").equals("karyawan")){
                    Session.set_username(rs.getString("nama_lengkap"));
                    dispose();
                    new Beranda_Karyawan().setVisible(true);
                }
                
            }else {
                validasiinput();
            }
        }catch (SQLException ex){
            Logger.getLogger(LoginB.class.getName()).log(Level.SEVERE,null, ex);
        }
    }else if(attempt != 7){
       //JOptionPane.showMessageDialog(this, "Kesempatan terakhir anda maka anda akan dialihkan ke frame forgot_password");
    }if(attempt == 7){
        dispose();
        new Forgot_Password().setVisible(true);    
    }
    attempt++;
    }//GEN-LAST:event_myButton1ActionPerformed

